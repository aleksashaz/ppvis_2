package data;

public class Stock {
    private int price;
    private int dividendValue;

    public int getDividendValue() {
        return dividendValue;
    }

    public int getPrice() {
        return price;
    }

    public void setDividendValue(int dividendValue) {
        this.dividendValue = dividendValue;
    }

    public void setPrice(int price) {
        this.price = price;
    }

}
